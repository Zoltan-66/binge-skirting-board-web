const NEEDS_CONFIRMATION = 'Needs factory confirmation';
const AVAILABLE_ON_REQUEST = 'Available on request';

function publicDimensions(value: string) {
  const lower = value.toLowerCase();
  if (lower.includes('confirm from sku artwork') || lower.includes('confirm section dimensions')) {
    return AVAILABLE_ON_REQUEST;
  }
  if (lower.includes('confirm')) return 'Request specification';
  return value;
}

function publicMaterial(value: string) {
  return value.toLowerCase().includes('to confirm') ? NEEDS_CONFIRMATION : value;
}

function publicFinishes(value: string) {
  return value.toLowerCase().includes('confirm') ? AVAILABLE_ON_REQUEST : value;
}

